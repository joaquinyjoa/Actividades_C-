namespace Geometria
{
    public class Punto
    {
        private int x;
        private int y;

        public int GetX()
        {
            return x;
        }

        public int GetY()
        {
            return y;
        }

        public Punto(int x, int y)
        {
            this.x = x;
            this.y = y;
        }
    }
}
